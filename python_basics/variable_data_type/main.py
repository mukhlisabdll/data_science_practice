# Use of variables
x = 8  # Integer variable
name = "Mukhlis Abdillah"  # String variable

# Printing the values of variables
print("x:", x)  # Output: 8
print("name:", name)  # Output: Mukhlis Abdillah

# Use of data types

# String data type (text)
name = "Bruce Wayne"  # Assigning a new string value
print("name (after change):", name)  # Output: Bruce Wayne
print("Type of name:", type(name))  # Output: <class 'str'>

# Integer data type (whole number)
age = 20  # Integer value
print("age:", age)  # Output: 20
print("Type of age:", type(age))  # Output: <class 'int'>

# Float data type (decimal number)
height = 191.5  # Floating-point value
print("height:", height)  # Output: 191.5
print("Type of height:", type(height))  # Output: <class 'float'>

# Boolean data type (True/False)
is_he_rich = True  # Boolean value
print("is_he_rich:", is_he_rich)  # Output: True
print("Type of is_he_rich:", type(is_he_rich))  # Output: <class 'bool'>

# List data type (ordered collection of items)
fruits = ["apple", "banana", "cherry"]  # List with 3 items
print("fruits:", fruits)  # Output: ['apple', 'banana', 'cherry']
print("Type of fruits:", type(fruits))  # Output: <class 'list'>

# Tuple data type (immutable ordered collection)
colors = ("red", "green", "blue")  # Tuple with 3 items
print("colors:", colors)  # Output: ('red', 'green', 'blue')
print("Type of colors:", type(colors))  # Output: <class 'tuple'>

# Dictionary data type (key-value pairs)
person = {"name": "John", "age": 30}  # Dictionary with key-value pairs
print("person:", person)  # Output: {'name': 'John', 'age': 30}
print("Type of person:", type(person))  # Output: <class 'dict'>

# Set data type (unordered collection of unique items)
unique_numbers = {1, 2, 3}  # Set with unique values
print("unique_numbers:", unique_numbers)  # Output: {1, 2, 3}
print("Type of unique_numbers:", type(unique_numbers))  # Output: <class 'set'>