# Latihan 1: Membuat Array Numpy dari Daftar Python

# Mengimpor library NumPy dan memberi alias np
import numpy as np

# Membuat list Python dengan elemen numerik
daftar = [1, 2, 3, 4, 5]

# Mengonversi list Python menjadi array NumPy
array = np.array(daftar)

# Menampilkan array NumPy
print("Array Numpy:", array)